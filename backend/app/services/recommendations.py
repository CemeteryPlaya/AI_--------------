"""
Правила бытовых рекомендаций для UI "Городской гид".

Здесь нет ML-модели: только явные пороги и эвристики,
чтобы поведение было предсказуемым и легко настраивалось.
"""


def get_clothing_recommendation(temp: float, wind_speed: float, humidity: int, aqi_index: int) -> list[str]:
    """
    Подбирает одежду по погодным условиям.

    Логика опирается на три фактора:
    1) температура (базовый слой одежды),
    2) ветер (нужна ли ветрозащита),
    3) влажность (риск осадков как грубый индикатор).

    Параметр `aqi_index` оставлен в сигнатуре намеренно:
    он нужен для совместимости API и возможного расширения правил.
    """
    items = []

    # Температура задает основной сценарий одежды.
    if temp < -15:
        items.append("Очень теплая зимняя куртка, термобелье")
        items.append("Теплая шапка, шарф и зимние перчатки")
    elif temp < 0:
        items.append("Зимняя куртка и теплые брюки")
        items.append("Шапка и перчатки")
    elif temp < 10:
        items.append("Осенняя куртка или пальто")
        items.append("Свитер или легкий джемпер")
    elif temp < 18:
        items.append("Легкая куртка или ветровка")
        items.append("Длинные брюки или джинсы")
    elif temp < 25:
        items.append("Футболка или рубашка с коротким рукавом")
        items.append("Легкие брюки или шорты")
    else:
        items.append("Легкая, дышащая летняя одежда")
        items.append("Головной убор (кепка/панама) и солнцезащитные очки")

    # Сильный ветер при плюсовой температуре: добавляем ветрозащиту.
    if wind_speed > 10 and temp > 10:
        items.append("Плотная ветровка (сильный ветер)")

    # Влажность >85% используем как простой прокси вероятных осадков.
    if humidity > 85:
        items.append("Возможно, понадобится зонт или непромокаемая обувь")

    return items


def get_departure_tips(aqi_index: int, temp: float, description: str) -> list[str]:
    """
    Формирует подсказки "перед выходом" по качеству воздуха и погоде.
    """
    tips = []

    # Шкала AQI OpenWeatherMap: 1..5, где 5 — самый загрязненный воздух.
    if aqi_index >= 4:
        tips.append("Качество воздуха плохое! Обязательно наденьте маску-респиратор (N95/FFP2).")
        tips.append("Ограничьте время пребывания на улице.")
        tips.append("Если у вас астма — проверьте наличие ингалятора.")
    elif aqi_index == 3:
        tips.append("Воздух умеренно загрязнен. Чувствительным людям стоит надеть маску.")

    # Ищем погодные маркеры и в русском, и в английском описании.
    text = description.lower()
    if "дожд" in text or "rain" in text:
        tips.append("На улице может быть дождь. Возьмите зонт.")
    if "снег" in text or "snow" in text:
        tips.append("Ожидается снег. Будьте осторожны на дорогах.")

    if temp > 30:
        tips.append("Очень жарко! Возьмите бутылку воды и нанесите солнцезащитный крем.")

    if not tips:
        tips.append("Погода располагает к прогулке. Отличного дня!")

    return tips
